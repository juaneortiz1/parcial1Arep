package org.example;

public class ReflexCalculator {
    public double add(double a, double b) {
        return a + b;
    }
    public double mul(double a, double b) {
        return a * b;
    }
    public double div(double a, double b) {
        return a / b;
    }
    public double res(double a, double b) {
        return a - b;
    }

}
